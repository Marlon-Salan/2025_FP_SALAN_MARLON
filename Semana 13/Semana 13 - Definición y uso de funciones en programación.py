# --- Funciones que reemplazan los bucles internos ---
def suma_temperaturas_semana(semana):
    """Devuelve la suma de temperaturas de una semana."""
    if not isinstance(semana, list) or len(semana) == 0:
        raise ValueError("Semana inválida: debe ser una lista no vacía de diccionarios con clave 'temp'.")
    try:
        return sum(dia["temp"] for dia in semana)
    except KeyError as e:
        raise KeyError(f"Falta la clave 'temp' en alguno de los diccionarios de la semana: {e}")
    except TypeError as e:
        raise TypeError(f"Formato de datos inválido en la semana: {e}")

def promedio_semana(semana):
    """Calcula el promedio de una semana usando suma_temperaturas_semana."""
    total = suma_temperaturas_semana(semana)
    return total / len(semana)

def promedios_por_ciudad(ciudad):
    """Devuelve una lista con los promedios semanales de una ciudad (sin usar bucle for explícito)."""
    # usamos comprensión (equivalente funcional a un bucle interno)
    return [promedio_semana(semana) for semana in ciudad]

# --- Función principal para mostrar promedios (usa las funciones previas) ---
def mostrar_promedios(temperaturas, ciudades):
    if len(temperaturas) != len(ciudades):
        raise ValueError("La cantidad de entradas en 'temperaturas' no coincide con la lista 'ciudades'.")
    for i, ciudad in enumerate(temperaturas):
        proms = promedios_por_ciudad(ciudad)
        print(f"\n {ciudades[i]}:")
        for j, p in enumerate(proms):
            print(f"   Semana {j+1}: Promedio = {p:.2f} °C")

# Ejecutar
if __name__ == "__main__":
    mostrar_promedios(temperaturas, ciudades)
